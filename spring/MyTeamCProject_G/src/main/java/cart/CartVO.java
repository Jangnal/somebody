package cart;

public class CartVO {

	String cart_title, filepath, filename, id, content;
	int point, cart_numb;
	
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCart_title() {
		return cart_title;
	}
	public void setCart_title(String cart_title) {
		this.cart_title = cart_title;
	}
	public String getFilepath() {
		return filepath;
	}
	public void setFilepath(String filepath) {
		this.filepath = filepath;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public int getCart_numb() {
		return cart_numb;
	}
	public void setCart_numb(int cart_numb) {
		this.cart_numb = cart_numb;
	}
	
	
	
}
